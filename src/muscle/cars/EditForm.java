    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        String car_id = carIdField.getText();
        
        String sql = "UPDATE cars SET year = ?, name = ?, model = ?, description = ?, pic = ? WHERE car_id = '"+ car_id +"'";
        
        try
        {
            
            pst = conn.prepareStatement(sql);
            
            pst.setString(1, yearTextField.getText());
            pst.setString(2, nameTextField.getText());
            pst.setString(3, modelTextField.getText());
            pst.setString(4, descriptionTextArea.getText());
            pst.setString(5, pic_file_name);
            
            pst.executeUpdate();
            descriptionTextArea.setCaretPosition(0);
            
        } // end try
        catch(Exception exception)
        {
            
            JOptionPane.showMessageDialog(null, exception);
            
        } // end catch Exception
        
    }//GEN-LAST:event_editButtonActionPerformed

    private void choosePicButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_choosePicButtonActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.showOpenDialog(null);
        File file = fileChooser.getSelectedFile();
        String fileName = file.getAbsolutePath();
        pic_file_name = file.getName();
        
        BufferedImage newImage = null;
        
        try
        {
            
            newImage = ImageIO.read(new File(fileName));
            
        } // end try
        catch(Exception exception)
        {
            
            JOptionPane.showMessageDialog(null, exception);
            
        } // end catch Exception
        
        Image scaledImage = newImage.getScaledInstance(pictureLabel.getWidth(), pictureLabel.getHeight(), Image.SCALE_SMOOTH);
        
        ImageIcon imageIcon = new ImageIcon(scaledImage);
        
        pictureLabel.setIcon(imageIcon);
    }//GEN-LAST:event_choosePicButtonActionPerformed

