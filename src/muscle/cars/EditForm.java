    public void showData(int car_id)
    {
        
        String sql = "SELECT * FROM cars WHERE car_id = '"+ car_id +"'";
        
        try
        {
            
            pst = conn.prepareStatement(sql);
            rs = pst.executeQuery();
            
            if(rs.next())
            {
                
                yearTextField.setText(rs.getString(2));
                nameTextField.setText(rs.getString(3));
                modelTextField.setText(rs.getString(4));
                descriptionTextArea.setText(rs.getString(5));

                BufferedImage image = null;
                
                try
                {
                    
                    image = ImageIO.read(new File("C:\\Users\\Adnan Alam\\Documents\\NetBeansProjects\\Muscle Cars\\pic\\" + rs.getString(6)));
                    
                } // end try
                catch(Exception exception)
                {
                    
                    JOptionPane.showMessageDialog(null, exception);
                    
                } // end catch Exception
                
                Image dimg = image.getScaledInstance(pictureLabel.getWidth(), pictureLabel.getHeight(), Image.SCALE_SMOOTH);
                
                ImageIcon icon = new ImageIcon(dimg);
                
                pictureLabel.setIcon(icon);
                
                pic_file_name = rs.getString(6);
                
                // pictureLabel.setIcon(new javax.swing.ImageIcon("C:\\Users\\Adnan Alam\\Documents\\NetBeansProjects\\Muscle Cars\\pic\\" + rs.getString(6)));
                
            } // end if
            else
            {
                
                JOptionPane.showMessageDialog(null, "No Car Id");
                
            } // end else
            
        } // end try
        catch(Exception exception)
        {
            
            JOptionPane.showMessageDialog(null, exception);
            
        } // end catch Exception
        
    } // end method showData

